package cn.sfw.zju.system.vo;

public class Interval {
	Integer csy_id;
	String cst_name;
	Double mdi_reading;
	String mdi_ts;
	public Integer getCsy_id() {
		return csy_id;
	}
	public void setCsy_id(Integer csy_id) {
		this.csy_id = csy_id;
	}
	public String getCst_name() {
		return cst_name;
	}
	public void setCst_name(String cst_name) {
		this.cst_name = cst_name;
	}
	public Double getMdi_reading() {
		return mdi_reading;
	}
	public void setMdi_reading(Double mdi_reading) {
		this.mdi_reading = mdi_reading;
	}
	public String getMdi_ts() {
		return mdi_ts;
	}
	public void setMdi_ts(String mdi_ts) {
		this.mdi_ts = mdi_ts;
	}
	
}
